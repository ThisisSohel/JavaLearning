public class variableDeclare {
    public static void main(String[] args) {
        int myNum = 5;
        float myFloatNum = 5.99f;
        char myLetter = 'D';
        boolean myBool = true;
        String myText = "Hello";

        System.out.println("The integer number is: " + myNum);
        System.out.println("The float number is: " + myFloatNum);
        System.out.println("The Character number is: " + myLetter);
        System.out.println("The Boolean number is: " + myBool);
        System.out.println("The String number is: " + myText);
    }
}
